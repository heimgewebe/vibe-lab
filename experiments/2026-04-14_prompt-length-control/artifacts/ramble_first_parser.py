import re

def extract_bold_text(text: str) -> list[str]:
    """
    Essay generated by LLM about Markup languages:
    Markup languages trace their roots back to the typesetting marks used by editors in the print industry...
    [...300 words of history of SGML, HTML, XML, and Markdown...]
    It was John Gruber who eventually popularized Markdown as a lightweight alternative...

    Now, here is the function.
    """
    # The model generated a lot of text, but didn't structurally think about the problem.
    # It tends to fall back to the same naive regex as Code-First, just after outputting a long essay.
    pattern = r'\*\*(.*?)\*\*'
    return re.findall(pattern, text)
