import re

def extract_bold_text(text: str) -> list[str]:
    """
    Spec generated by LLM:
    1. Bold text starts with exactly two unescaped asterisks **
    2. Bold text ends with exactly two unescaped asterisks **
    3. Asterisks preceded by a backslash (\*) are escaped and do not count.
    4. Content inside can contain anything including escaped asterisks.
    """
    # Negative lookbehind to ensure the starting ** is not preceded by \
    # Negative lookbehind to ensure the ending ** is not preceded by \
    pattern = r'(?<!\\)\*\*(.*?)(?<!\\)\*\*'

    matches = re.findall(pattern, text, flags=re.DOTALL)
    return matches
